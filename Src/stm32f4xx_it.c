/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */
	ticks++;
	in1 = HAL_GPIO_ReadPin(B1_GPIO_Port, B1_Pin);

	if (in1==0)
	{
		in1_0++;
		in1_1 = 0;
		if (in1_0 >= REF_BOUNCE)
		{
			in1_0 = REF_BOUNCE + 1;
			statoIn1 = 0;
			if (push_state !=1)
			{
				push_state = 1;
				printf("Start of pushing\n\r");
				start_push_tick = millis();
				end_push_tick = 0;
			}
		}
	}
	else
	{
		in1_0 = 0;
		in1_1++;
		if (in1_1 >= REF_BOUNCE)
		{
			in1_1 = REF_BOUNCE + 1;
			statoIn1 = 1;

			if (push_state != 0)
			{
				end_push_tick = millis();
				push_state = 0;
				uint32_t push_duration2 = end_push_tick - start_push_tick;
				printf("push_duration in %d ms\n\r", push_duration2);
				start_push_tick=0;
			}

		}
	}
  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */

  /* USER CODE END SysTick_IRQn 1 */
}

